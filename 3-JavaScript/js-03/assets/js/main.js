/* Bucles */ 
/* Estructura que nos permite repetir nuestro código */

/* For */
/* De que es un ciclo controlado */

/* Se le tiene que indicar lo siguiente:
    * La variable con la que se va a trabajar i, j, k, l
    * Condición para que se ejecute
    * El cambio de nuestra variable de trabajo
*/
for(let i=0; i<10; i++){
    console.log("El valor de i es" + i);
}


/* While */
/* Porque es un ciclo no controlado */
/* Se necesita:
    +Una condición => True o False */
/* Mientras */

let saludo;

while (saludo !== "Hola"){

    saludo=prompt("Saludame");

}